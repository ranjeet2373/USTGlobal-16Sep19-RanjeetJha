package com.ustglobal.collectionframework.list;

public class Employee2 {
	int id;
	String name;
	String company;
	
	
	public Employee2(int id, String name, String company) {
		super();
		this.id = id;
		this.name = name;
		this.company = company;
	}


	@Override
	public String toString() {
		return "Employee2 [id=" + id + ", name=" + name + ", company=" + company + "]";
	}
	
	
	
	

}
